let num1 = 7.4548957;

// arredonda um número para baixo, obtendo um valor inteiro
console.log(Math.floor(num1));

// arredonda um número para cima, obtendo um valor inteiro
console.log(Math.ceil(num1));

// arrendonda um número para o mais proximo, obtendo um valor inteiro
console.log(Math.round(num1))

// recebe um array de numbers e retorna/pega o maior numero da sequencia
console.log(Math.max(1, 2, 3, 50, 60, 34, 500, 45.039));

// pega um numero aleatório
console.log(Math.round(Math.random() * 10));

